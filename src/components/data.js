const size = sentence.length;

export function ChangeLettersToUnreadable(props) {
  let haslo1 = "";
  for (let index = 0; index < size; index++) {
    if (props[index] === " ") {
      haslo1 += " ";
    } else {
      haslo1 += "-";
    }
  }
  return haslo1;
}

export function checkSentence(state, sentence, letter) {
  for (let index = 0; index < state.length ; index++) {
    if (sentence.charAt(index) === letter){
      state = changeLetterIfExists(state, index, letter);
    }
  }
  return state;
}

export function changeLetterIfExists(state, index, letter) {
  if (index > state.length - 1) return state;
  else return state.substr(0, index) + letter + state.substr(index + 1);
}

export function changeSentenceState(state, sentence, letter) {
  return checkSentence(state, sentence, letter);
}
